# ============ 假流式工具 ============

async def stream_template(text: str, chunk_size: int = 2, delay: float = 0.02) -> AsyncIterator[str]:
    """把固定模板按字符切片 yield，模拟打字机效果，让前端体验和真流式一致。"""
    for i in range(0, len(text), chunk_size):
        yield text[i : i + chunk_size]
        await asyncio.sleep(delay)
